#!/usr/bin/env node
/**
 * build-save-extension.mjs
 * Usage:
 *   node build-save-extension.mjs           — patch bump (production)
 *   node build-save-extension.mjs minor     — minor bump
 *   node build-save-extension.mjs major     — major bump
 *   node build-save-extension.mjs --no-bump — no version change
 *   node build-save-extension.mjs --dev     — dev config (API_BASE_DEV), no bump
 */

import { readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'
import { API_BASE, ENV } from './lib/env.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const MANIFEST_PATH = join(__dirname, 'extension', 'manifest.json')
const CONFIG_PATH   = join(__dirname, 'extension', 'config.js')

const args    = process.argv.slice(2)
const isDev   = args.includes('--dev')
const noBump  = args.includes('--no-bump') || isDev
const bumpType = args.find(a => ['major', 'minor', 'patch'].includes(a)) ?? 'patch'

// API_BASE is already resolved correctly by scripts/env.mjs based on PEERMESH_ENV.
// --dev overrides to use API_BASE_DEV explicitly.
const appUrl = isDev
  ? (process.env.API_BASE_DEV ?? API_BASE)
  : API_BASE

writeFileSync(CONFIG_PATH,
  `// extension/config.js — generated by build-save-extension.mjs\n` +
  `// Do not edit manually. Run: node build-save-extension.mjs [--dev]\n` +
  `export const APP_URL = '${appUrl}'\n` +
  `export const IS_DEV = ${isDev}\n`
)
console.log(`  ✓ config.js → APP_URL=${appUrl} IS_DEV=${isDev} (PEERMESH_ENV=${ENV})`)

const manifest = JSON.parse(readFileSync(MANIFEST_PATH, 'utf-8'))
const currentVersion = manifest.version
const [major, minor, patch] = currentVersion.split('.').map(Number)

let newVersion = currentVersion
if (!noBump) {
  if (bumpType === 'major')      newVersion = `${major + 1}.0.0`
  else if (bumpType === 'minor') newVersion = `${major}.${minor + 1}.0`
  else                           newVersion = `${major}.${minor}.${patch + 1}`
  manifest.version = newVersion
  writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2) + '\n')
  console.log(`\n  Version: ${currentVersion} → ${newVersion}`)
} else {
  console.log(`\n  Version: ${currentVersion} (no bump)`)
}

console.log(`  ✓ manifest.json updated`)
if (isDev) {
  console.log(`  Dev build ready — load extension/ at chrome://extensions (unpacked)\n`)
} else {
  console.log(`  Reload the extension at chrome://extensions then deploy with: npx vercel --prod\n`)
}
